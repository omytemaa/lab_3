#!/bin/bash
set -e

echo "==================================="
echo "🧪 Этап 2: Тестирование программы"
echo "==================================="

PROGRAM="./src/lab2"
PASSED=0
FAILED=0

# Функция для запуска теста
run_test() {
    local test_name="$1"
    local input="$2"
    local expected="$3"
    
    echo "Тест: $test_name"
    
    # Запускаем программу с входными данными и сохраняем вывод
    output=$(echo -e "$input" | $PROGRAM 2>&1)
    
    # Показываем вывод программы для отладки
    echo "   Вывод программы:"
    echo "$output" | sed 's/^/   /'
    
    # Проверяем наличие ожидаемого текста в выводе
    if echo "$output" | grep -q "$expected"; then
        echo "  ✅ ПРОЙДЕН"
        PASSED=$((PASSED+1))
    else
        echo "  ❌ ПРОВАЛЕН"
        echo "     Ожидалось: $expected"
        FAILED=$((FAILED+1))
    fi
    echo ""
}

# Тест 1: Обычный случай
run_test "Обычный случай" \
    "3\n5 10 15\n3\n10 20 30" \
    "Наибольший элемент в K, отсутствующий в L: 15"

# Тест 2: Все элементы K есть в L
run_test "Все элементы есть в L" \
    "3\n1 2 3\n3\n1 2 3" \
    "Нет элемента в K, отсутствующего в L"

# Тест 3: Ни одного совпадения
run_test "Нет совпадений" \
    "3\n1 2 3\n3\n4 5 6" \
    "Наибольший элемент в K, отсутствующий в L: 3"

# Тест 4: Один элемент в K
run_test "Один элемент в K" \
    "1\n7\n3\n4 5 6" \
    "Наибольший элемент в K, отсутствующий в L: 7"

echo "==================================="
echo "ИТОГИ ТЕСТИРОВАНИЯ:"
echo "✅ Пройдено: $PASSED"
echo "❌ Провалено: $FAILED"
echo "==================================="

if [ $FAILED -eq 0 ]; then
    echo "✅ ВСЕ ТЕСТЫ ПРОЙДЕНЫ!"
    exit 0
else
    echo "❌ ЕСТЬ ОШИБКИ!"
    exit 1
fi
